// logCaptureMutex ensures that only one captureLogEntries call can run at a time
// to prevent log entries from parallel test runs from ending up in wrong buffers
var logCaptureMutex sync.Mutex

// ThreadSafeBuffer wraps bytes.Buffer with a mutex to make it thread-safe
type ThreadSafeBuffer struct {
	buf bytes.Buffer
	mu  sync.RWMutex
}

func (tsb *ThreadSafeBuffer) Write(p []byte) (n int, err error) {
	tsb.mu.Lock()
	defer tsb.mu.Unlock()
	return tsb.buf.Write(p)
}

func (tsb *ThreadSafeBuffer) String() string {
	tsb.mu.RLock()
	defer tsb.mu.RUnlock()
	return tsb.buf.String()
}

// Helper to capture log entries generated by the logger and return it as string
func captureLogEntries(run func()) string {
	logCaptureMutex.Lock()
	defer logCaptureMutex.Unlock()

	f := log.StandardLogger().Formatter
	log.SetFormatter(&log.TextFormatter{DisableColors: true})
	defer log.SetFormatter(f)

	output := &ThreadSafeBuffer{}
	original := log.StandardLogger().Out
	log.SetOutput(output)
	defer log.SetOutput(original)

	run()

	return output.String()
}

// newDestinationClusterClient returns a fake clientset that rejects Event creates when
// involvedObject.namespace does not match the Event namespace, mirroring apiserver validation.
func newDestinationClusterClient() *fake.Clientset {
	c := fake.NewClientset()
	c.PrependReactor("create", "events", func(action k8stesting.Action) (bool, runtime.Object, error) {
		createAction, ok := action.(k8stesting.CreateAction)
		if !ok {
			return false, nil, nil
		}
		ev, ok := createAction.GetObject().(*corev1.Event)
		if !ok {
			return false, nil, nil
		}
		eventNamespace := createAction.GetNamespace()
		if ev.InvolvedObject.Namespace != "" && ev.InvolvedObject.Namespace != eventNamespace {
			return true, nil, apierrors.NewInvalid(
				schema.GroupKind{Group: "", Kind: "Event"},
				ev.Name,
				field.ErrorList{
					field.Invalid(
						field.NewPath("involvedObject", "namespace"),
						ev.InvolvedObject.Namespace,
						"does not match event.namespace",
					),
				},
			)
		}
		return false, nil, nil
	})
	return c
}

