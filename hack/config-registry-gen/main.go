// Command config-registry-gen emits coverage-only registry descriptors for
// argocd-cmd-params-cm keys into util/configbus/cmd_params_generated.go.
//
//	go run ./hack/config-registry-gen -repo-root .
package main

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"

	"gopkg.in/yaml.v3"
)

// skipCMKeys are registered by hand elsewhere and must not be re-emitted here.
// Do not consult the live registry — that would make regeneration wipe the file.
var skipCMKeys = map[string]bool{
	"timeout.reconciliation":        true,
	"timeout.hard.reconciliation":   true,
	"timeout.reconciliation.jitter": true,
	// Application-controller settings with a durable home (cmd_params_controller.go).
	"controller.metrics.cluster.labels":          true,
	"controller.self.heal.timeout.seconds":       true,
	"controller.sync.timeout.seconds":            true,
	"controller.repo.error.grace.period.seconds": true,
	"controller.resource.health.persist":         true,
	"controller.diff.server.side":                true,
	"controller.ignore.normalizer.jq.timeout":    true,
}

func main() {
	repoRoot := "."
	if len(os.Args) > 1 {
		for i := 0; i < len(os.Args); i++ {
			if os.Args[i] == "-repo-root" && i+1 < len(os.Args) {
				repoRoot = os.Args[i+1]
			}
		}
	}

	keys := map[string]string{} // cmKey -> envVar
	must(collectDocKeys(filepath.Join(repoRoot, "docs/operator-manual/argocd-cmd-params-cm.yaml"), keys))
	must(collectManifestEnvMaps(filepath.Join(repoRoot, "manifests/base"), keys))

	var entries []cmdParamEntry
	for cmKey, env := range keys {
		if skipCMKeys[cmKey] {
			continue
		}
		entries = append(entries, cmdParamEntry{
			Name:      camelName(cmKey),
			CMKey:     cmKey,
			EnvVar:    env,
			Component: inferComponent(cmKey),
		})
	}
	sort.Slice(entries, func(i, j int) bool { return entries[i].CMKey < entries[j].CMKey })

	// Deduplicate names.
	seenName := map[string]int{}
	for i := range entries {
		base := entries[i].Name
		if n, ok := seenName[base]; ok {
			seenName[base] = n + 1
			entries[i].Name = fmt.Sprintf("%s_%d", base, n+1)
		} else {
			seenName[base] = 1
		}
	}

	var buf bytes.Buffer
	buf.WriteString("// Code generated by hack/config-registry-gen; DO NOT EDIT.\n\n")
	buf.WriteString("package configbus\n\n")
	buf.WriteString("func init() {\n")
	buf.WriteString("\tregisterCmdParamsGenerated()\n")
	buf.WriteString("}\n\n")
	buf.WriteString("func registerCmdParamsGenerated() {\n")
	for _, e := range entries {
		fmt.Fprintf(&buf, "\tregisterCmdParam(%q, %q, %q, %q)\n", e.Name, e.CMKey, e.EnvVar, e.Component)
	}
	buf.WriteString("}\n")

	out := filepath.Join(repoRoot, "util/configbus/cmd_params_generated.go")
	must(os.WriteFile(out, buf.Bytes(), 0o644))
	fmt.Fprintf(os.Stderr, "wrote %s (%d descriptors)\n", out, len(entries))
}

type cmdParamEntry struct {
	Name      string
	CMKey     string
	EnvVar    string
	Component string
}

func must(err error) {
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

func collectDocKeys(path string, keys map[string]string) error {
	data, err := os.ReadFile(path)
	if err != nil {
		return err
	}
	var doc map[string]any
	if err := yaml.Unmarshal(data, &doc); err != nil {
		return err
	}
	dataMap, _ := doc["data"].(map[string]any)
	for k := range dataMap {
		if _, ok := keys[k]; !ok {
			keys[k] = ""
		}
	}
	return nil
}

func collectManifestEnvMaps(baseDir string, keys map[string]string) error {
	return filepath.Walk(baseDir, func(path string, info os.FileInfo, err error) error {
		if err != nil || info.IsDir() {
			return err
		}
		if !strings.HasSuffix(path, ".yaml") && !strings.HasSuffix(path, ".yml") {
			return nil
		}
		data, err := os.ReadFile(path)
		if err != nil {
			return err
		}
		if !strings.Contains(string(data), "argocd-cmd-params-cm") {
			return nil
		}
		dec := yaml.NewDecoder(bytes.NewReader(data))
		for {
			var doc any
			if err := dec.Decode(&doc); err != nil {
				break
			}
			walkCMKeyRefs(doc, keys)
		}
		return nil
	})
}

func walkCMKeyRefs(v any, keys map[string]string) {
	switch t := v.(type) {
	case map[string]any:
		if envName, ok := t["name"].(string); ok {
			if vf, ok := t["valueFrom"].(map[string]any); ok {
				if cmr, ok := vf["configMapKeyRef"].(map[string]any); ok {
					cmName, _ := cmr["name"].(string)
					key, _ := cmr["key"].(string)
					if key != "" && strings.Contains(cmName, "cmd-params") {
						keys[key] = envName
					}
				}
			}
		}
		for _, child := range t {
			walkCMKeyRefs(child, keys)
		}
	case []any:
		for _, child := range t {
			walkCMKeyRefs(child, keys)
		}
	}
}

var nonIdent = regexp.MustCompile(`[^a-zA-Z0-9]+`)

func camelName(cmKey string) string {
	parts := nonIdent.Split(cmKey, -1)
	var b strings.Builder
	for i, p := range parts {
		if p == "" {
			continue
		}
		if i == 0 {
			b.WriteString(strings.ToLower(p[:1]))
			if len(p) > 1 {
				b.WriteString(p[1:])
			}
			continue
		}
		b.WriteString(strings.ToUpper(p[:1]))
		if len(p) > 1 {
			b.WriteString(p[1:])
		}
	}
	name := b.String()
	if name == "" {
		return "cmdParam"
	}
	if name[0] >= '0' && name[0] <= '9' {
		name = "p" + name
	}
	return name
}

func inferComponent(cmKey string) string {
	switch {
	case strings.HasPrefix(cmKey, "controller."):
		return "controller"
	case strings.HasPrefix(cmKey, "server."):
		return "server"
	case strings.HasPrefix(cmKey, "reposerver."):
		return "reposerver"
	case strings.HasPrefix(cmKey, "applicationsetcontroller."):
		return "applicationset"
	case strings.HasPrefix(cmKey, "notificationscontroller."):
		return "notifications"
	case strings.HasPrefix(cmKey, "commitserver."), strings.HasPrefix(cmKey, "commit."):
		return "commitserver"
	case strings.HasPrefix(cmKey, "dexserver."):
		return "dex"
	default:
		return "shared"
	}
}
